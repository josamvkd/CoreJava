package com.example.corejava;

public class EvenOrOdd2 {
    public static void main(String[] args) {
        int n=0;
        if(n==0){
            System.out.println("Zero is not an Even or Odd");
        }else if(n%2==0){
            System.out.println("Even Number");
        }else{
            System.out.println("Odd Number");
        }
    }
}
